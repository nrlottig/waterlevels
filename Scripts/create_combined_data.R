library(tidyverse)
library(lubridate)
library(gridExtra)
library(Metrics)
library(cowplot)

waterlevel <- read.csv("big_data/seepage_20180414.csv",sep=",",header=TRUE) %>% 
  mutate(Date = as.Date(as.character(Date),format= "%m/%d/%Y")) %>% 
  mutate(Value = as.numeric(as.character(Value))*304.8)  %>% 
  select(WBIC,WiscID,Date,Value) %>% 
  mutate(Year=year(Date)) %>% 
  drop_na()
crosswalk <- waterlevel %>% select(WBIC,WiscID) %>% distinct()

process_extrapolations <- read_csv("data/process_extrapolations.csv") %>% 
  rename(level_process = level) %>% 
  group_by(WBIC) %>% 
  mutate(level_process = scale(level_process,center = T,scale = F)) %>% 
  ungroup() %>% 
  filter(Year >=1960)

cdev_lakelevel_predictions <- read_csv("data/cdev_lakelevel_predictions.csv") %>% 
  mutate(Year = year(Date)) %>% 
  select(WiscID,Year,yhat) %>% 
  rename(level_precip = yhat) %>% 
  mutate(level_precip = level_precip/10) %>% 
  group_by(WiscID,Year) %>% 
  summarize(level_precip = mean(level_precip)) %>% 
  mutate(level_precip = scale(level_precip,center = T,scale = F)) %>% 
  left_join(crosswalk) %>% 
  ungroup() %>% 
  select(-WiscID)



#identify longest record for comparision purposes
best.lakes <- waterlevel %>% 
  group_by(WBIC,WiscID) %>% 
  summarize(total_n=n(),dat_yr = length(unique(Year))) %>% 
  ungroup() %>% 
  group_by(WBIC) %>% 
  filter(dat_yr == max(dat_yr)) %>% 
  filter(total_n == max(total_n)) %>% 
  ungroup()
#filter water level records
waterlevel <- waterlevel %>% filter(WiscID %in% best.lakes$WiscID)
waterlevel <- waterlevel %>% 
  group_by(WBIC,Year) %>% 
  summarize(Value = mean(Value,na.rm=TRUE)/10) %>% 
  mutate(level_obs = scale(Value,center = T,scale = F)) %>% 
  select(-Value)

#create master data frame and scale all observations to cdev estimates

dat_offset <- cdev_lakelevel_predictions %>% full_join(waterlevel) %>% 
  mutate(diff = level_precip - level_obs) %>% 
  group_by(WBIC) %>% 
  summarize(offset = mean(diff,na.rm=TRUE)) %>% 
  drop_na()

dat <- cdev_lakelevel_predictions %>% 
  full_join(waterlevel) %>% 
  left_join(dat_offset) %>% 
  mutate(level_obs = level_obs + offset) %>% 
  select(-offset)

dat_offset <- cdev_lakelevel_predictions %>% full_join(process_extrapolations) %>% 
  mutate(diff = level_precip - level_process) %>% 
  group_by(WBIC) %>% 
  summarize(offset = mean(diff,na.rm=TRUE)) %>% 
  drop_na()

dat <- dat %>% full_join(process_extrapolations) %>% 
  left_join(dat_offset) %>% 
  mutate(level_process = level_process + offset) %>% 
  select(-offset)

dat <- dat %>% filter(Year <=2015)
dat2 <- dat
dat <- dat %>%
  gather(key = method,value = value,-Year,-WBIC) %>% 
  arrange(WBIC)

dat <- dat %>% group_by(WBIC) %>% 
  mutate(n = sum(!is.na(value))) %>% 
  filter(n > 0) %>% 
  select(-n)
dat3 <- dat %>% spread(key = method,value=value)
write_csv(dat3,"data/combined_waterlevels.csv")

lakes <- unique(dat$WBIC)
lake_info <- read.csv("big_data/seepage_20180414.csv",sep=",",header=TRUE) %>% 
  select(WBIC,SiteName) %>% distinct() %>%
  group_by(WBIC) %>% 
  slice(1) %>% 
  ungroup()
z=1
plot = list()

pdf("graphics/water_level_summary.pdf",width=8,height=10.5,onefile = TRUE)
for(i in 1:length(lakes)) {
  temp.dat <- dat %>% filter(WBIC==lakes[i])
  lake.info <- lake_info %>% filter(WBIC==lakes[i])
  plot[[z]] <- ggplot(data = temp.dat, aes(x=Year,y=value,color=method)) + geom_line() + geom_point() +
    labs(title = paste(lake.info[[1]],": ",lake.info[[2]],sep=""),y = "Annual Water Level Anomaly (cm)")
  if (z %% 3 == 0) { ## print 8 plots on a page
    do.call(grid.arrange,  c(plot,ncol=1))
    plot = list() # reset plot 
    z = 0 # reset index
  }
  z = z + 1
}    
if (length(plot) != 0) { do.call(grid.arrange,  c(plot,ncol=1)) }
dev.off()

precip <- dat3 %>% select(level_obs,level_precip) %>% drop_na()
rmse_precip <- round(rmse(actual = precip$level_obs,predicted = precip$level_precip),digits=1)

p1 <- ggplot(data = dat3,aes(x=level_obs,y=level_precip)) + geom_point(alpha=0.3) +
  xlim(-200,200) + ylim(-200,200) + geom_abline(slope = 1,intercept = 0) +
  labs(x="Observed Level (cm)",y="Modeled Level (cm)") +
  annotate("text",x=100,y=-200,label = paste("RMSE: ",rmse_precip,sep=""))
p1

process <- dat3 %>% select(level_obs,level_process) %>% drop_na()
rmse_process <- round(rmse(actual = process$level_obs,predicted = process$level_process),digits=1)
p2 <- ggplot(data = dat3,aes(x=level_obs,y=level_process)) + geom_point(alpha=0.3) +
  xlim(-200,200) + ylim(-200,200) + geom_abline(slope = 1,intercept = 0) +
  labs(x="Observed Level (cm)",y="Modeled Level (cm)") +
  annotate("text",x=100,y=-200,label = paste("RMSE: ",rmse_process,sep=""))
p2

plots <- plot_grid(p1,p2,labels=c("A","B"))
plots
save_plot(plots,base_aspect_ratio = 2,base_height = 4,dpi=300,filename = "graphics/rmse.png")
